# Boolean Logic

a = 5
b = -1

# boolean type
print(a == b)   
print(a > b)
print(a < b)
print(5 > 5)    # false
print(5 >= 5)   # True


# Нөхцөл шалгах - if, elif, else statement
if a == b:
    print("a is equal to b")
elif a > b:
    print("a is bigger than b")
else:
    print("b is bigger than a")
